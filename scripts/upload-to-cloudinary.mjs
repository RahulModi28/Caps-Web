/**
 * scripts/upload-to-cloudinary.mjs
 *
 * One-time upload script: uploads all images from public/images/ to Cloudinary
 * and generates the config/images.ts file with the resulting URLs.
 *
 * Run with: node scripts/upload-to-cloudinary.mjs
 */

import { v2 as cloudinary } from "cloudinary";
import dotenv from "dotenv";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

dotenv.config({ path: ".env.local" });

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const IMAGES_DIR = path.join(__dirname, "../public/images");
const CONFIG_OUTPUT = path.join(__dirname, "../config/images.ts");
const CLOUDINARY_FOLDER = "caps-website";

cloudinary.config({
  cloud_name: process.env.CLOUDINARY_CLOUD_NAME,
  api_key: process.env.CLOUDINARY_API_KEY,
  api_secret: process.env.CLOUDINARY_API_SECRET,
});

// Map filename (without ext) to a clean variable name
function toVarName(filename) {
  return filename
    .replace(/\.[^.]+$/, "") // remove extension
    .replace(/[-_](.)/g, (_, c) => c.toUpperCase()) // camelCase
    .replace(/^./, (c) => c.toLowerCase()); // lowercase first char
}

async function uploadImage(filePath, publicId) {
  console.log(`⬆️  Uploading: ${path.basename(filePath)} ...`);
  const result = await cloudinary.uploader.upload(filePath, {
    public_id: publicId,
    folder: CLOUDINARY_FOLDER,
    overwrite: true,
    resource_type: "image",
    // Auto quality + format for best performance
    transformation: [{ quality: "auto", fetch_format: "auto" }],
  });
  console.log(`   ✅ Done → ${result.secure_url}`);
  return result;
}

async function main() {
  if (!fs.existsSync(IMAGES_DIR)) {
    console.error(`❌ Images directory not found: ${IMAGES_DIR}`);
    process.exit(1);
  }

  const files = fs.readdirSync(IMAGES_DIR).filter((f) => {
    return /\.(jpg|jpeg|png|webp|gif|svg)$/i.test(f) && !f.startsWith(".");
  });

  if (files.length === 0) {
    console.log("No images found in public/images/");
    process.exit(0);
  }

  console.log(`\n🚀 Uploading ${files.length} image(s) to Cloudinary...\n`);

  const results = [];

  for (const file of files) {
    const filePath = path.join(IMAGES_DIR, file);
    const baseName = file.replace(/\.[^.]+$/, ""); // no extension
    const publicId = baseName; // keeps the clean name in Cloudinary
    const varName = toVarName(baseName);

    try {
      const result = await uploadImage(filePath, publicId);
      results.push({
        varName,
        fileName: file,
        url: result.secure_url,
        publicId: result.public_id,
      });
    } catch (err) {
      console.error(`   ❌ Failed to upload ${file}:`, err.message);
    }
  }

  // Generate config/images.ts
  const lines = [
    `/**`,
    ` * config/images.ts`,
    ` *`,
    ` * Central image registry — all site images are defined here.`,
    ` * To swap an image: log into https://cloudinary.com/console,`,
    ` * upload the new photo to the "${CLOUDINARY_FOLDER}" folder with the same`,
    ` * Public ID, then the site will automatically serve the new image.`,
    ` *`,
    ` * Auto-generated by: node scripts/upload-to-cloudinary.mjs`,
    ` * Last updated: ${new Date().toISOString()}`,
    ` */`,
    ``,
    `const CLOUD = "https://res.cloudinary.com/${process.env.CLOUDINARY_CLOUD_NAME}/image/upload";`,
    `const OPT = "f_auto,q_auto,w_auto"; // auto format, quality & width`,
    ``,
    `export const images = {`,
  ];

  for (const r of results) {
    lines.push(`  /** ${r.fileName} */`);
    lines.push(`  ${r.varName}: \`\${CLOUD}/\${OPT}/${r.publicId}\`,`);
    lines.push(``);
  }

  lines.push(`};`);
  lines.push(``);
  lines.push(`export default images;`);
  lines.push(``);

  fs.writeFileSync(CONFIG_OUTPUT, lines.join("\n"), "utf8");

  console.log(`\n📄 config/images.ts generated with ${results.length} entries.`);
  console.log(`\n✨ All done! Now update your HTML src values to use images from config/images.ts\n`);

  // Print a quick summary
  console.log("--- CLOUDINARY URLs ---");
  for (const r of results) {
    console.log(`${r.varName}: ${r.url}`);
  }
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
